export type Translations = typeof TRANSLATIONS['fr'];

/* ── Context ─────────────────────────────────────────────────── */
interface LangContextType {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: Translations;
}

const LangContext = React.createContext<LangContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = React.useState<Lang>('fr');

  React.useEffect(() => {
    const saved = localStorage.getItem('gcfi-lang') as Lang | null;
    if (saved === 'en' || saved === 'fr') {
      setLangState(saved);
    } else {
      // Détection automatique de la langue du navigateur
      const browserLang = navigator.language || '';
      if (browserLang.startsWith('en')) {
        setLangState('en');
      }
      // Par défaut : français
    }
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem('gcfi-lang', l);
  };

  return (
    <LangContext.Provider value={{ lang, setLang, t: TRANSLATIONS[lang] as Translations }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  const ctx = React.useContext(LangContext);
  if (!ctx) throw new Error('useLang must be used within LanguageProvider');
  return ctx;
}